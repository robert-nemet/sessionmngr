func TestSaveSession(t *testing.T) {
	tmpDir := t.TempDir()
	testutil.SetEnvForTest(t, config.EnvStorageLocation, tmpDir)
	cfg := config.NewConfig()

	now := time.Now()

	// Create session without UUID (storage will generate it)
	sess := &session.Session{
		UUID:          "",
		Title:         "Test Session",
		CreatedAt:     now,
		LastUpdatedAt: now,
		Tags:          []string{"test"},
		Messages: []session.Message{
			{Role: "user", Content: "Test question"},
			{Role: "assistant", Content: "Test answer"},
		},
	}

	store := NewBasicStorage(cfg)
	savedSess, err := store.SaveSession(context.Background(), sess)
	require.NoError(t, err)

	uuid := savedSess.UUID
	require.NotEmpty(t, uuid, "UUID should be generated by storage")

	// Verify raw.json was created
	filePath := filepath.Join(tmpDir, uuid, "raw.json")
	data, err := os.ReadFile(filePath)
	require.NoError(t, err, "Failed to read raw.json")

	var loaded session.Session
	require.NoError(t, json.Unmarshal(data, &loaded))

	assert.Equal(t, uuid, loaded.UUID)
	assert.Equal(t, "Test Session", loaded.Title)
	assert.Len(t, loaded.Messages, 2)

	// Verify index.json was created
	indexPath := filepath.Join(tmpDir, "index.json")
	indexData, err := os.ReadFile(indexPath)
	require.NoError(t, err, "Failed to read index.json")

	var idx SessionIndex
	require.NoError(t, json.Unmarshal(indexData, &idx))

	require.Len(t, idx.Sessions, 1)
	assert.Equal(t, uuid, idx.Sessions[0].UUID)
}

