func generateTestUUID(i int) string {
	// Generate unique UUIDs for testing
	base := "550e8400-e29b-41d4-a716-"
	suffix := ""
	for len(suffix) < 12 {
		suffix = suffix + string(rune('0'+i%10))
		i = i / 10
	}
	return base + suffix
}
